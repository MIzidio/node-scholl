def printt(n):
    fizz = 'fizz'
    buzz = 'buzz'
    fb = 'fizzbuzz'
    while n <= 100:
        if n%3 == 0:
            return fizz
            ++n
        elif n%5 == 0:
            return buzz
            ++n
        elif n%3 == 0 and n%5 == 0:
            return fb
            ++n
        else:
            return n
            ++n
            